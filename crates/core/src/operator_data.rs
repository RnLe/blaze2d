/// Which inner product convention is in use.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum InnerProductKind {
    /// Standard unweighted ⟨u|v⟩ (TE mode).
    Standard,
    /// B-weighted ⟨u|B|v⟩ where B = ε(r) (TM mode).
    BWeighted,
}

impl InnerProductKind {
    pub fn from_polarization(pol: Polarization) -> Self {
        match pol {
            Polarization::TE => Self::Standard,
            Polarization::TM => Self::BWeighted,
        }
    }

    pub fn label(&self) -> &'static str {
        match self {
            Self::Standard => "standard",
            Self::BWeighted => "B_weighted",
        }
    }
}

/// Exact hermitized TM ingredients needed for the O(eta^2) TM Hamiltonian.
#[derive(Debug, Clone)]
pub struct OperatorDataExactTM {
    /// Hermitized local Bloch states \tilde{u}_alpha = sqrt(epsilon) u_alpha.
    /// Shape: [n_total][grid_size].
    pub hermitized_eigenvectors: Vec<Vec<Complex64>>,

    /// Exact hermitized TM velocity matrices \tilde{v}^{(i)}_{alpha beta}.
    /// Shape per direction: [n_total x n_total], row-major.
    pub velocity_matrices: [Vec<Complex64>; 2],

    /// Matrix elements of \partial_{R_i} \tilde{L}^{(0)}_{TM}.
    /// Shape per direction: [n_total x n_total], row-major.
    pub local_r_derivative_matrices: [Vec<Complex64>; 2],

    /// Matrix elements of \partial_{R_i}^2 \tilde{L}^{(0)}_{TM}.
    /// Shape per direction: [n_total x n_total], row-major.
    pub local_r_second_derivative_matrices: [Vec<Complex64>; 2],

    /// Exact first-order remainder matrix \tilde{Gamma}^{(1)}_{alpha beta}.
    /// Shape: [n_total x n_total], row-major.
    pub first_order_remainder: Vec<Complex64>,

    /// Exact direct second-order metric overlap \tilde{G}_{mn}.
    /// Shape: [n_retained x n_retained], row-major.
    pub direct_metric: Vec<Complex64>,

    /// Exact direct second-order one-derivative matrices \tilde{B}^{(i)}_{mn}.
    /// Shape per direction: [n_retained x n_retained], row-major.
    pub direct_b_matrices: [Vec<Complex64>; 2],

    /// Exact direct second-order scalar matrix \tilde{Gamma}^{(2)}_{mn}.
    /// Shape: [n_retained x n_retained], row-major.
    pub direct_gamma2: Vec<Complex64>,

    /// Exact Hermitian inverse-mass tensor built from \tilde{G} and remote-band curvature.
    /// Shape: [2][2][n_retained x n_retained], row-major in band indices.
    pub mass_tensor_inv: [[Vec<Complex64>; 2]; 2],

    /// First slow derivatives of epsilon.
    pub epsilon_r_derivatives: [Vec<f64>; 2],

    /// Second slow derivatives of epsilon.
    pub epsilon_r_second_derivatives: [Vec<f64>; 2],

    /// First slow derivatives of rho = epsilon^(-1/2).
    pub rho_r_derivatives: [Vec<f64>; 2],

    /// Second slow derivatives of rho = epsilon^(-1/2).
    pub rho_r_second_derivatives: [Vec<f64>; 2],
}

/// All EA Hamiltonian ingredients extracted at one (R, k₀) point.
///
/// Matrix elements are stored in **row-major** order: for a matrix M_{mn},
/// the element at (m, n) is at index `m * n_cols + n`.
///
/// # Units
///
/// | Quantity | Units | Description |
/// |----------|-------|-------------|
/// | eigenvalues | (2π/a)² | λ = (ω/c)², same as eigensolver output |
/// | velocity_matrices | (2π/a)² per (2π/a) = (2π/a) | ∂λ/∂k derivative units |
/// | w_matrices | (2π/a)² per (2π/a)² = dimensionless | ∂²λ/∂k² units |
/// | mass_tensor_inv | same as w_matrices | Löwdin-corrected ∂²λ/∂k² |
/// | r_derivative_matrices | (2π/a)² / fractional | effective ∂L_eff/∂R, R in frac. coords |
/// | metric_derivative_matrices | dimensionless / fractional | ∂B/∂R, zero for TE |
/// | berry_connection_matrices | 1 / fractional | gauge-fixed Hermitian A_j in retained space |
/// | born_huang | (2π/a)² | scalar potential units |
/// | slow_coefficient_potential | (2π/a)² | scalar potential units |
/// | k0 | 2π/a | Cartesian k-vector |
/// | registry | fractional | atom shift in lattice units |
#[derive(Debug, Clone)]
pub struct OperatorData {
    // -- Metadata --
    pub polarization: Polarization,
    pub inner_product: InnerProductKind,
    /// Carrier momentum k₀ in Cartesian reciprocal-space units (2π/a).
    pub k0: [f64; 2],
    /// Registry point (fractional atom shift coordinates).
    pub registry: [f64; 2],
    /// Index of the first retained band in the full eigensystem.
    pub band_lo: usize,
    pub n_retained: usize,
    pub n_remote: usize,
    pub grid_dims: [usize; 2],

    // -- Eigensystem --
    /// Eigenvalues λₙ for ALL n_total = n_retained + n_remote bands.
    /// Units: (2π/a)².
    pub eigenvalues: Vec<f64>,

    /// Eigenvectors as solver-grid field values. Shape: [n_total][grid_size].
    /// Complex64 values in the same representation as the solver output.
    pub eigenvectors: Vec<Vec<Complex64>>,

    // -- First-order: velocity matrices --
    /// v^(i)_{mn} = ⟨uₘ|∂L₀/∂kᵢ|uₙ⟩
    /// Shape per direction: [n_retained × n_total], row-major.
    /// Units: (2π/a) (eigenvalue per k-unit).
    pub velocity_matrices: [Vec<Complex64>; 2],

    // -- Second-order: raw operator 2nd derivatives --
    /// w^(ij)_{mn} = ⟨uₘ|∂²L₀/∂kᵢ∂kⱼ|uₙ⟩
    /// Shape: [n_retained × n_retained], row-major.
    /// Units: dimensionless (eigenvalue per k²-unit).
    pub w_matrices: [[Vec<Complex64>; 2]; 2],

    // -- Second-order: Löwdin-corrected mass tensor --
    /// M⁻¹_{ij,mn} including remote-band Löwdin sums.
    /// Shape: [n_retained × n_retained], row-major.
    /// Units: dimensionless.
    pub mass_tensor_inv: [[Vec<Complex64>; 2]; 2],

    // -- Registry-derivative matrix elements --
    /// ⟨uₘ|∂L₀/∂Rⱼ|uₙ⟩, shape: [n_retained × n_total], row-major.
    /// For TE this is the direct operator derivative ⟨uₘ|∂L₀/∂Rⱼ|uₙ⟩.
    /// For TM this is the effective generalized-eigenproblem perturbation
    /// ⟨uₘ|∂L_eff/∂Rⱼ|uₙ⟩ = -λₙ⟨uₘ|∂B/∂Rⱼ|uₙ⟩.
    /// Units: (2π/a)² / fractional.
    /// None if R-derivatives were not requested.
    pub r_derivative_matrices: Option<[Vec<Complex64>; 2]>,

    /// Metric-derivative matrices ⟨uₘ|∂B/∂Rⱼ|uₙ⟩, shape: [n_retained × n_total],
    /// row-major. For TE the metric is identity, so this is identically zero.
    /// Units: 1 / fractional.
    /// None if R-derivatives were not requested.
    pub metric_derivative_matrices: Option<[Vec<Complex64>; 2]>,

    /// Berry connection matrices A^(j) in the retained space, shape:
    /// [n_retained × n_retained], row-major. Off-diagonal entries follow the
    /// perturbative formulas above; diagonals are fixed to zero gauge.
    /// Units: 1 / fractional.
    /// None if R-derivatives were not requested.
    pub berry_connection_matrices: Option<[Vec<Complex64>; 2]>,

    // -- Born–Huang potential --
    /// Φ_{mn}(R), shape: [n_retained × n_retained], row-major.
    /// Units: (2π/a)².
    /// None if Born–Huang was not requested.
    pub born_huang: Option<Vec<Complex64>>,

    /// Direction-resolved Born–Huang tensor Φ^(ij)_{mn}(R), with shape
    /// [2][2][n_retained × n_retained], row-major in band indices.
    /// The scalar Born–Huang matrix above is the native-basis trace
    /// Φ^(xx) + Φ^(yy). Phase 2 can contract this tensor with the moiré metric
    /// exactly after coordinate transforms.
    pub born_huang_tensor: Option<[[Vec<Complex64>; 2]; 2]>,

    /// U_sc,mn(R), shape: [n_retained × n_retained], row-major.
    /// This is currently implemented only for TE, where it is built from the
    /// explicit registry dependence of ε⁻¹ in the second-order operator.
    /// Units: (2π/a)².
    /// None if the term was not requested or is unavailable for the active
    /// polarization.
    pub slow_coefficient_potential: Option<Vec<Complex64>>,

    /// Direction-resolved slow-coefficient tensor U_sc^(ij)_{mn}(R), with
    /// shape [2][2][n_retained × n_retained], row-major in band indices.
    /// The scalar slow-coefficient matrix above is the native-basis trace
    /// U_sc^(xx) + U_sc^(yy).
    pub slow_coefficient_tensor: Option<[[Vec<Complex64>; 2]; 2]>,

    // -- Exact TE downfolding remainder blocks --
    /// Exact TE first-order scalar block Ξ_sc^(1) from Appendix A, shape
    /// [n_retained × n_retained], row-major. This is the local scalar matrix
    /// before adding the covariantization terms that produce U_rem^(1).
    pub xi_scalar_first_order: Option<Vec<Complex64>>,

    /// Exact TE direct second-order coefficient K_i = U^† ε^{-1} Ξ_i for the
    /// retained manifold, one matrix per slow direction, each with shape
    /// [n_retained × n_retained], row-major.
    pub kappa_matrices: Option<[Vec<Complex64>; 2]>,

    /// Exact TE weighted leakage scalar Σ_i Ξ_i^† ε^{-1} Ξ_i in the retained
    /// manifold, shape [n_retained × n_retained], row-major.
    pub weighted_leakage_scalar: Option<Vec<Complex64>>,

    /// Exact TE Löwdin coupling blocks T_i = -i Q V_i U, stored as remote ×
    /// retained matrices with shape [n_remote × n_retained], row-major.
    pub lowdin_t_matrices: Option<[Vec<Complex64>; 2]>,

    /// Exact TE remote scalar block R from Appendix A, stored as a remote ×
    /// retained matrix with shape [n_remote × n_retained], row-major.
    pub lowdin_r_matrix: Option<Vec<Complex64>>,

    // -- Overlap matrix for gauge transport --
    /// S_{mn} = ⟨uₘ(R)|uₙ(R')⟩, shape: [n_retained × n_retained], row-major.
    /// None if overlap was not requested.
    pub overlap_matrix: Option<Vec<Complex64>>,

    /// Exact hermitized TM export block for the O(eta^2) TM pipeline.
    /// None for TE or when the required slow dielectric derivatives were not computed.
    pub exact_tm: Option<OperatorDataExactTM>,

    // -- Solver metadata --
    pub n_iterations: usize,
    pub converged: bool,
}

