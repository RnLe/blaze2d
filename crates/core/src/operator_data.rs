/// Configuration for what to extract.
#[derive(Debug, Clone)]
pub struct OperatorDataConfig {
    /// Index of the first retained band in the full eigensystem.
    /// Bands 0..band_lo become lower-remote, bands band_lo..band_lo+n_retained
    /// are the retained manifold, and bands band_lo+n_retained..n_total are
    /// upper-remote. Default: 0 (lowest bands retained).
    pub band_lo: usize,
    /// Number of bands to keep in the retained (active) subspace.
    pub n_retained: usize,
    /// Number of additional upper-remote bands to compute for Löwdin sums.
    pub n_remote: usize,
    /// Whether to compute the Löwdin-corrected mass tensor.
    pub compute_mass_tensor: bool,
    /// Whether to compute Born–Huang matrix elements.
    pub compute_born_huang: bool,
    /// Whether to compute the slow-coefficient potential U_sc.
    pub compute_slow_coefficient: bool,
    /// Whether to compute overlap matrix with reference eigenvectors.
    pub compute_overlap: bool,
}

impl Default for OperatorDataConfig {
    fn default() -> Self {
        Self {
            band_lo: 0,
            n_retained: 4,
            n_remote: 8,
            compute_mass_tensor: true,
            compute_born_huang: false,
            compute_slow_coefficient: false,
            compute_overlap: false,
        }
    }
}

